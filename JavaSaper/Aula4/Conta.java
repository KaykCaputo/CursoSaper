package Aula4;

public class Conta {
    private String name;
    private double saldo;
    public String getName() {
        return name;
    }
    public void setName(String nomeCliente) {
        this.name = nomeCliente;
    }
    public double getSaldo() {
        return saldo;
    }
    public void setSaldo(Double nsaldo) {
        this.saldo = nsaldo;
    }
}
